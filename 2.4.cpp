#include <iostream>
#include <string>
#include <vector>

using namespace std;

class Person {
    string IdPerson, Name, Sex, Birthday, HomeAddress;

   public:
    Person() {}
    Person(string IdPerson,
           string Name,
           string Sex,
           string Birthday,
           string HomeAddress) {
        this->IdPerson = IdPerson;
        this->Name = Name;
        this->Sex = Sex;
        this->Birthday = Birthday;
        this->HomeAddress = HomeAddress;
    }
    ~Person() {}
    void Print() {
        cout << IdPerson << " " << Name << " " << Sex << " " << Birthday << " "
             << HomeAddress << endl;
    }
    void getPerson() {
        cin >> IdPerson >> Name >> Sex >> Birthday >> HomeAddress;
    }
};

int main() {
    vector<Person*> ps;
    string IdPerson, Name, Sex, Birthday, HomeAddress;
    while (cin >> IdPerson >> Name >> Sex >> Birthday >> HomeAddress) {
            ps.push_back(new Person(IdPerson, Name, Sex, Birthday, HomeAddress));
    }
    for (auto p : ps)
        p->Print();
    return 0;
}